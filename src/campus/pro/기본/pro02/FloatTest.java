package campus.pro.기본.pro02;

class FloatTest {
    public static void main(String[] args) {
        float fl = 123.456F; //4byte = int와 메모리 크기가 동일하다.
        System.out.println("fl: "+fl);

        float f1 = 2100000000;
        System.out.println("f1: "+f1);

        float f2 = 21.00001F;
        System.out.println("f2: "+f2);

        float f3 = 1234567;
        System.out.println("f3: "+f3);
    }
}
